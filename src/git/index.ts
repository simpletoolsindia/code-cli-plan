import { readFileSync, writeFileSync, existsSync } from 'node:fs'
import { resolve } from 'node:path'

const execAsync = promisify(spawn)

export interface GitConfig {
  // Attribution flags (6-flag system)
  attributeAuthor: boolean      // GIT_AUTHOR_NAME
  attributeCommitter: boolean  // GIT_COMMITTER_NAME
  attributeCoAuthoredBy: boolean  // Co-authored-by trailer
  attributeCommitMessageAuthor: boolean  // Include in commit message author
  attributeCommitMessageCommitter: boolean  // Include in commit message committer

  // Other options
  autoCommit: boolean
  commitPrompt: string
  subtreeOnly: boolean
  gitCommitVerify: boolean  // Respect pre-commit hooks

  // User info
  userName: string
  userEmail: string
}

export interface CommitResult {
  success: boolean
  hash?: string
  message?: string
  error?: string
}

export interface DiffResult {
  staged: string[]
  unstaged: string[]
  untracked: string[]
  changed: string[]
}

